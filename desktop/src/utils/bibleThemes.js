// Levenshtein distance for fuzzy matching
function getLevenshteinDistance(a, b) {
    if (a.length === 0) return b.length;
    if (b.length === 0) return a.length;
    const matrix = [];
    for (let i = 0; i <= b.length; i++) {
        matrix[i] = [i];
    }
    for (let j = 0; j <= a.length; j++) {
        matrix[0][j] = j;
    }
    for (let i = 1; i <= b.length; i++) {
        for (let j = 1; j <= a.length; j++) {
            if (b.charAt(i - 1) === a.charAt(j - 1)) {
                matrix[i][j] = matrix[i - 1][j - 1];
            } else {
                matrix[i][j] = Math.min(
                    matrix[i - 1][j - 1] + 1,
                    matrix[i][j - 1] + 1,
                    matrix[i - 1][j] + 1
                );
            }
        }
    }
    return matrix[b.length][a.length];
}

// Fuzzy theme finder: exact match > substring match > Levenshtein typo tolerance
export function findTheme(query) {
    const q = query.toLowerCase().trim();
    if (!q) return null;

    // 1. Exact match
    if (bibleThemes[q]) return bibleThemes[q];

    // 2. Substring match — if user query is contained in a theme key (or vice versa)
    const keys = Object.keys(bibleThemes);
    for (const key of keys) {
        if (key.includes(q) && q.length >= 5) return bibleThemes[key];
        if (q.includes(key) && key.length >= 5) return bibleThemes[key];
    }

    // 3. Levenshtein fuzzy match — tolerate typos
    let bestMatch = null;
    let minDistance = Infinity;
    for (const key of keys) {
        const dist = getLevenshteinDistance(q, key);
        if (dist < minDistance) {
            minDistance = dist;
            bestMatch = key;
        }
    }
    // Scale allowed typos by query length
    const allowedTypos = q.length > 10 ? 3 : (q.length > 6 ? 2 : 1);
    if (minDistance <= allowedTypos && bestMatch) {
        return bibleThemes[bestMatch];
    }

    return null;
}